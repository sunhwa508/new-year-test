export interface ScoreMap {
  [key: string]: number;
}

export function calculateSemesterResult(scores: ScoreMap): SemesterResult {
  let maxScore = -1;
  let resultType = "chill";

  for (const [type, score] of Object.entries(scores)) {
    if (score > maxScore && semesterResults[type]) {
      maxScore = score;
      resultType = type;
    }
  }

  return semesterResults[resultType];
}
